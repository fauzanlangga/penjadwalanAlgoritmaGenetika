                  <h2>Sistem Penjadwalan Algoritma Genetika</h2>
                  <p>Algoritma genetika adalah algoritma pencarian yang berbasis pada mekanisme seleksi alam & genetika. Algoritma genetika tepat digunakan dalam menyelesaikan masalah optimasi kompleks, yang sulit dilakukan oleh metode konvensional. Algoritma ini diperkenalkan oleh John Holland (1975) dari Universitas Michigan.<br/><br/>
                  Sifat algoritma genetika mencari kemungkinan dari calon solusi untuk mendapatkan solusi optimal dalam penyelesaian masalah. Ruang cakupan dari semua solusi yang layak, yaitu berbagai obyek diantara solusi yang sesuai, yang dinamakan ruang pencarian. Tiap titik didalam ruang pencarian mempresentasikan satu solusi yang layak.<br/><br/>
                  Tiap solusi yang layak ditandai dengan nilai fitnessnya. Solusi yang dicari adalah titik (satu/lebih) diantara solusi yang layak dalam ruang pencarian. Aplikasi ini merupakan Implementasi Algoritma Genetika dalam Penjadwalan Perkuliahan pada Program Studi Teknik Informatika Universitas Sangga Buana YPKP Bandung.</p>
